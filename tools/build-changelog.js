#!/usr/bin/env node
/**
 * build-changelog.js — Parses docs/CHANGELOG.md into a typed TS module.
 *
 * Output: src/renderer/generated/changelog.ts
 * Format: Keep a Changelog (https://keepachangelog.com/)
 *
 * Run: node tools/build-changelog.js
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const INPUT = path.join(ROOT, 'docs', 'CHANGELOG.md');
const OUT_DIR = path.join(ROOT, 'src', 'renderer', 'generated');
const OUTPUT = path.join(OUT_DIR, 'changelog.ts');

const SECTION_HEADING_RE = /^##\s+\[(.+?)\](?:\s+-\s+(\d{4}-\d{2}-\d{2}))?/;
const CATEGORY_RE = /^###\s+(\S.*?)\s*$/;
const KNOWN_CATEGORIES = new Set(['added', 'changed', 'fixed', 'removed']);
const BULLET_RE = /^[-*]\s+(.+)$/;

/** @typedef {{ version: string; date?: string; added?: string[]; changed?: string[]; fixed?: string[]; removed?: string[] }} Entry */

/** @param {string} md @returns {{ entries: Entry[]; warnings: string[] }} */
function parse(md) {
  const lines = md.split(/\r?\n/);
  /** @type {Entry[]} */
  const entries = [];
  /** @type {string[]} */
  const warnings = [];

  /** @type {Entry | null} */
  let current = null;
  /** @type {keyof Entry | null} */
  let currentCat = null;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    const sectionMatch = SECTION_HEADING_RE.exec(line);
    if (sectionMatch) {
      if (current) entries.push(current);
      const version = sectionMatch[1].toLowerCase() === 'unreleased'
        ? 'unreleased'
        : sectionMatch[1];
      current = { version, date: sectionMatch[2] };
      currentCat = null;
      continue;
    }

    if (!current) continue;

    const catMatch = CATEGORY_RE.exec(line);
    if (catMatch) {
      const cat = catMatch[1].toLowerCase();
      if (KNOWN_CATEGORIES.has(cat)) {
        currentCat = /** @type {any} */ (cat);
      } else {
        warnings.push(`Line ${i + 1}: unrecognised category "${catMatch[1]}" — skipping`);
        currentCat = null;
      }
      continue;
    }

    if (currentCat) {
      const bulletMatch = BULLET_RE.exec(line);
      if (bulletMatch) {
        const key = /** @type {any} */ (currentCat);
        if (!current[key]) current[key] = [];
        current[key].push(bulletMatch[1].trim());
      }
    }
  }

  if (current) entries.push(current);
  return { entries, warnings };
}

/** @param {Entry} e @returns {string} */
function serializeEntry(e) {
  const fields = [
    `version: ${JSON.stringify(e.version)}`,
    e.date ? `date: ${JSON.stringify(e.date)}` : null,
    e.added?.length ? `added: ${JSON.stringify(e.added)}` : null,
    e.changed?.length ? `changed: ${JSON.stringify(e.changed)}` : null,
    e.fixed?.length ? `fixed: ${JSON.stringify(e.fixed)}` : null,
    e.removed?.length ? `removed: ${JSON.stringify(e.removed)}` : null,
  ].filter(Boolean);
  return `{ ${fields.join(', ')} }`;
}

/** @param {Entry[]} entries @returns {string} */
function render(entries) {
  const recordLines = entries
    .map(e => `  ${JSON.stringify(e.version)}: ${serializeEntry(e)},`)
    .join('\n');
  const orderLiteral = JSON.stringify(entries.map(e => e.version));

  return [
    '// AUTO-GENERATED by tools/build-changelog.js. DO NOT EDIT.',
    'export interface ChangelogEntry {',
    '  version: string;',
    '  date?: string;',
    '  added?: string[];',
    '  changed?: string[];',
    '  fixed?: string[];',
    '  removed?: string[];',
    '}',
    `export const CHANGELOG: Record<string, ChangelogEntry> = {`,
    recordLines,
    '};',
    `export const VERSION_ORDER: readonly string[] = ${orderLiteral};`,
    '',
  ].join('\n');
}

function main() {
  if (!fs.existsSync(INPUT)) {
    process.stderr.write(`build-changelog: ERROR — ${INPUT} not found\n`);
    process.exit(1);
  }

  const md = fs.readFileSync(INPUT, 'utf8');
  const { entries, warnings } = parse(md);

  for (const w of warnings) {
    process.stderr.write(`build-changelog: WARNING — ${w}\n`);
  }

  if (entries.length === 0) {
    process.stderr.write('build-changelog: WARNING — no version entries found\n');
  }

  if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });

  fs.writeFileSync(OUTPUT, render(entries), 'utf8');
  process.stdout.write(`build-changelog: wrote ${entries.length} entries → ${OUTPUT}\n`);
}

main();
